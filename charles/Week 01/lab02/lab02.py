def fib(n):
    """Returns the nth Fibonacci number.

    >>> fib(0)
    0
    >>> fib(1)
    1
    >>> fib(2)
    1
    >>> fib(3)
    2
    >>> fib(4)
    3
    >>> fib(5)
    5
    >>> fib(6)
    8
    """
    cur, prev = 1,0
    ct = 0
    while ct < n:
        cur,prev = cur+prev,cur
        ct+=1
    print(prev)

